# 14626_ISBN

# 입력받은 ISBN 문자열에서 '-'를 제거한다.
# 예: "978-1-2345-6789-0" → "9781234567890"
isbn = input().replace('-', '')

# ISBN-13은 반드시 13자리여야 한다.
# 하이픈 제거 후 길이가 13이 아니면 잘못된 입력이므로 예외 발생
if len(isbn) != 13:
    raise ValueError("ISBN은 하이픈 제거 후 13자리여야 합니다.")

# 문제 조건에 따르면 '*'는 정확히 한 개 존재해야 한다.
# 0개이거나 2개 이상이면 문제 조건 위반
if isbn.count('*') != 1:
    raise ValueError("훼손된 문자는 '*' 1개여야 합니다.")

# 문자열은 수정이 불편하므로 리스트로 변환
# 각 자리를 개별적으로 접근하기 위함
isbn_list = list(isbn)

# '*'가 위치한 인덱스를 찾는다.
# 예: "978*234567890" → 인덱스 3
missing_idx = isbn_list.index('*')

# 문제에서 '*'는 체크기호(마지막 자리)가 아니라고 했음.
# ISBN-13의 마지막 자리(인덱스 12)는 체크기호이므로,
# 방어적으로 마지막 자리에 '*'가 오면 예외 처리
if missing_idx == 12:
    raise ValueError("'*'는 체크기호(마지막 자리)가 아닙니다.")

# '*'를 제외한 나머지 자리들의 가중합을 저장할 변수
total = 0

# 각 자리의 숫자와 인덱스를 함께 순회
for i, ch in enumerate(isbn_list):

    # 현재 문자가 '*'이면 계산에서 제외하고 다음 반복으로 이동
    if ch == '*':
        continue

    # 숫자 문자이므로 정수로 변환
    digit = int(ch)

    # ISBN-13 가중치 규칙:
    # 인덱스가 짝수이면 가중치 1
    # 인덱스가 홀수이면 가중치 3
    weight = 1 if i % 2 == 0 else 3

    # 해당 자리의 (숫자 × 가중치)를 누적합에 더한다.
    total += digit * weight

# '*'가 위치한 자리의 가중치를 계산
# 나중에 x를 대입할 때 사용할 값
missing_weight = 1 if missing_idx % 2 == 0 else 3

# '*' 자리에 들어갈 숫자 x는 0~9 중 하나
# 모든 경우를 대입하여 조건을 만족하는 값 찾기
for x in range(10):

    # 전체 합 = 기존 합 + (x × 해당 자리 가중치)
    # ISBN-13 유효 조건: 전체합 % 10 == 0
    if (total + x * missing_weight) % 10 == 0:

        # 조건을 만족하는 숫자를 출력
        print(x)

        # 정답은 유일하므로 반복 종료
        break